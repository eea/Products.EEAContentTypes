def fix_categories_maps(self):
    """ Recategorize some maps as graphs
    """
    uids = filter(None, _UIDS.split("\n"))
    catalog = self.portal_catalog

    i = 0
    for uid in uids:
        obj = catalog.searchResults(UID=uid)[0].getObject()
        obj.setFigureType('graph')
        obj.reindexObject()
        logging.info("Changed figure type for %s", obj)
        if i % 10 == 0:
            transaction.commit()

    logging.info("Done changing figure types")
    return "Done"

def update_publications_subobjects(self):
    """ Update publications subobjects
    """
    cat = getToolByName(self, 'portal_catalog')
    query = {'Language': 'all',
             'portal_type': ['Report']}

    brains = cat(**query)
    count = 0
    logger.info('Start updating subobjects for all Publications')
    for brain in brains:
        logger.info('Updating %s' % brain.getURL())
        obj = brain.getObject()
        obj.setConstrainTypesMode(0)
        count += 1
        if count == 10:
            count = 0
            transaction.commit()

    logger.info('Done updating subobjects for all Publications')
    return "Done"
